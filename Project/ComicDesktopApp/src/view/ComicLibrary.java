    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:              
        lblMessage.setText("");
        DefaultTableModel model=(DefaultTableModel) tblComic.getModel();
        if(!txtTitel.getText().trim().equals("")){
            model.addRow(new Object[]{cbxUitgever.getSelectedItem().toString(),txtTitel.getText(),
                txtPrijs.getText(), txtISBN.getText()});
        }
        else{
            lblMessage.setText("Titel moet worden ingegeven");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblComicMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblComicMouseClicked
        // TODO add your handling code here:
        TableModel model=(TableModel) tblComic.getModel();
        cbxUitgever.setSelectedItem(model.getValueAt(tblComic.getSelectedRow(), 0).toString());
        txtTitel.setText(model.getValueAt(tblComic.getSelectedRow(), 1).toString());
        txtPrijs.setText(model.getValueAt(tblComic.getSelectedRow(), 2).toString());
        txtISBN.setText(model.getValueAt(tblComic.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_tblComicMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        TableModel model=(TableModel) tblComic.getModel();
        if(tblComic.getSelectedRow()==-1){
            if(tblComic.getRowCount() ==0) {
                lblMessage.setText("Bibliotheek is leeg");
            }else{
                lblMessage.setText("Je moet een comic selecteren");
            }
        }else{
            model.setValueAt(cbxUitgever.getSelectedItem().toString(), tblComic.getSelectedRow(), 0);
            model.setValueAt(txtTitel.getText(), tblComic.getSelectedRow(), 1);
            model.setValueAt(txtPrijs.getText(), tblComic.getSelectedRow(), 2);
            model.setValueAt(txtISBN.getText(), tblComic.getSelectedRow(), 3);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

