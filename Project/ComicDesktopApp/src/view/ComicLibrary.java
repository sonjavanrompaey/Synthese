    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel) tblComic.getModel();
        if(tblComic.getSelectedRow()==-1){
            if(tblComic.getRowCount() ==0) {
                lblMessage.setText("Bibliotheek is leeg");
            }else{
                lblMessage.setText("Je moet een comic selecteren");
            }
        }else{
            model.removeRow(tblComic.getSelectedRow());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


